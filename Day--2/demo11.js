function add(a,b){
    return a+b;
}
function sub(a,b){
    return a-b;
}
function mul(a,b){
    return a*b;
}
function div(a,b){
    return a/b;
}

let res=add(10,20);
console.log(`Addition is: ${res}`);

res=sub(10,20);
console.log(`substraction is: ${res}`);

res=mul(10,20);
